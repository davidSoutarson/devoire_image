<h3> Demande</h3>
<p>
Vous devez créer un script de rangement d’images (en vrac) en fonction de leur nom.
Le but de ce script est de ranger (déplacer) chaque image du dossier vrac/ dans un répertoire dédié à chaque série,
 d’après le nom de l’image.

Par exemple, les images vrac/montagne_2009-001.jpg et vrac/montagne_2009-002.jpg seront automatiquement déplacées,
à l’exécution du script de rangement, dans un répertoire montagne_2009/.</p>

<p>
  L’utilisation des fonctions rename (https://www.php.net/manual/fr/function.rename.php)
  et explode (https://www.php.net/manual/fr/function.explode.php) sera utile à ce travail.
</p>

